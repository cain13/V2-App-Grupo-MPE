
CREATE TABLE IF NOT EXISTS usuariosTable (
	IdUsuarioTable INTEGER PRIMARY KEY AUTOINCREMENT,
	Usuario TEXT  NOT NULL,
	Password_ INTEGER NOT NULL,
    FingerID BOOLEAN DEFAULT NULL,
	Tipo TEXT NOT NULL,
    Nombre TEXT NOT NULL,
	Recordarme BOOLEAN DEFAULT NULL,
	EsBuzo BOOLEAN DEFAULT NULL,
	EsGuardiaCivil BOOLEAN DEFAULT NULL
);

CREATE TABLE IF NOT EXISTS notificacion (
	IdNotificacion INTEGER PRIMARY KEY AUTOINCREMENT,
	Titulo TEXT  NOT NULL,
	Mensaje TEXT  NOT NULL,
    Leido INTEGER DEFAULT NULL,
	TipoDocumento TEXT NOT NULL,
    Fecha TEXT NOT NULL,
	Ruta TEXT NOT NULL,
	Icono TEXT NOT NULL
);