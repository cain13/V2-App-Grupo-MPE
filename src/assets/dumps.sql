	IdUsuarioTable INTEGER PRIMARY KEY AUTOINCREMENT,
	Usuario TEXT  NOT NULL,
	Password_ INTEGER NOT NULL,
    FingerID BOOLEAN DEFAULT NULL,
	Tipo TEXT NOT NULL,
    Nombre TEXT NOT NULL,
	Recordarme BOOLEAN DEFAULT NULL,
	EsBuzo BOOLEAN DEFAULT NULL,
	EsGuardiaCivil BOOLEAN DEFAULT NULL,
    RequiereMantoux BOOLEAN DEFAULT NULL,
    Email TEXT DEFAULT NULL,
    Movil TEXT DEFAULT NULL,
    Telefono TEXT DEFAULT NULL,
    RecordarEditarPerfil BOOLEAN DEFAULT NULL

