CREATE TABLE IF NOT EXISTS notificacion (
	IdNotificacion INTEGER PRIMARY KEY AUTOINCREMENT,
	Titulo TEXT  NOT NULL,
	Mensaje TEXT  NOT NULL,
    Leido INTEGER DEFAULT NULL,
	TipoDocumento TEXT NOT NULL,
    Fecha TEXT NOT NULL,
	Ruta TEXT NOT NULL,
	Icono TEXT NOT NULL
);

